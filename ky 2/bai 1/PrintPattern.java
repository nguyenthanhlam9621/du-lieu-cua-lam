public class PrintPattern {
    public static void main(String[] args){
        System.out.println("* * * * *");
        System.out.println(" * * * * *");
        System.out.println("* * * * *");
        System.out.println(" * * * * *");
        System.out.println("* * * * *");
        System.out.println();
        System.out.println("* * * * *");
        System.out.println("*       *");
        System.out.println("*       *");
        System.out.println("*       *");
        System.out.println("* * * * *");
        System.out.println();
        System.out.println("* * * * *");
        System.out.println(" *     * ");
        System.out.println("  *   *  ");
        System.out.println("   * *   ");
        System.out.println("    *    ");
        System.out.println();
        System.out.println("    *    ");
        System.out.println("* *   * *");
        System.out.println("   * *   ");
        System.out.println("  *   *  ");
        System.out.println(" *     * ");
        System.out.println();
        System.out.println("         '  '");
        System.out.println("          -- ");
        System.out.println("         (oo)");
        System.out.println("  /========//");
        System.out.println(" / || @@ ||  ");
        System.out.println("*  ||----||  ");
        System.out.println("   VV    VV  ");
        System.out.println("   ''    ''  ");

    }
}
